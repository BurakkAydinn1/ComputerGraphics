#ifndef VEC3_HPP
#define VEC3_HPP

#include <cmath>
#include <iostream>

class Vec3
{
public:
    float x, y, z;

    Vec3();
    Vec3(float x, float y, float z);

    Vec3 operator+(const Vec3 &v) const;
    Vec3 operator-(const Vec3 &v) const;
    Vec3 operator-() const;
    Vec3 operator*(float scalar) const;
    Vec3 operator/(float scalar) const;
    Vec3 operator*(const Vec3 &v) const;
    Vec3 &operator+=(const Vec3 &v);

    float dot(const Vec3 &v) const;
    Vec3 cross(const Vec3 &v) const;
    float length() const;
    Vec3 normalize() const;

    void print() const;
};

#endif
